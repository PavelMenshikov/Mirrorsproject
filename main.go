		uniqueVisitors[userID] = struct{}{} // –î–æ–±–∞–≤–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
		log.Printf("–ù–æ–≤—ã–π —É–Ω–∏–∫–∞–ª—å–Ω—ã–π –ø–æ—Å–µ—Ç–∏—Ç–µ–ª—å: %d. –í—Å–µ–≥–æ —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö: %d\n", userID, len(uniqueVisitors))
